"""Tests for hello function."""
import pytest

from ascii_parser.parser import parse


def test_parse():
    art = """
    .##..#....###..####.#..#.####.#..#.#..#.
    #..#.#....#..#.#....#.#..#....#.#..#..#.
    #..#.#....#..#.###..##...###..##...#..#.
    ####.#....###..#....#.#..#....#.#..#..#.
    #..#.#....#.#..#....#.#..#....#.#..#..#.
    #..#.####.#..#.####.#..#.#....#..#..##..
    """
    s = parse(art)
    assert s == "ALREKFKU"
